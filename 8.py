#Create a set of numbers. Add a new number, then remove a number, and print the final set.

set_of_numbers = {1, 2, 3, 4, 5}
print("Orignal set:", set_of_numbers)
set_of_numbers.add(6)
print("Set after adding 6:", set_of_numbers)
set_of_numbers.remove(3)
print("Set after removing 3:", set_of_numbers)
print("Final set:", set_of_numbers)