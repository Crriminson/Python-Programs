#Write a program to display the data type of different literals (e.g., integer, float, string, boolean). 

integer_literal = 42
float_literal = 3.14
string_literal = "Hello, World!"
boolean_literal = True

print(f"Data type of integer_literal '{integer_literal}': {type(integer_literal)}")
print(f"Data type of float_literal '{float_literal}': {type(float_literal)}")
print(f"Data type of string_literal '{string_literal}': {type(string_literal)}")
print(f"Data type of boolean_literal '{boolean_literal}: {type(boolean_literal)}")